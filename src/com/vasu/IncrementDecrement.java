package com.vasu;
public class IncrementDecrement {
    public static void main(String[] args) {
        int x =10;
        int y= ++x;
        System.out.println(y);
        int x1=10;
        int y1=x1++;
        System.out.println(y1);
        int x3=10;
        int y3=x3--;
        System.out.println(y3);
        int x4=10;
        int y4=--x4;
        System.out.println(y4);
    }
}
